/* modelrxnrate */
double modelrxnrate(double a,double b,double difc,double chi) {
	double rate;

	if(b<0) rate=4*PI*difc*a*chi;
	else if(b<=a) rate=-1;
	else rate=4*PI*difc*a*b*chi/(b-a);
	return rate; }


